// While Loop

public class Loops {
    public static void main(String[] args) {
        int count = 0;
        while (count < 5) {
            System.out.println("Count is: " + count);
            count++;
        }
    }
}


//Do while Loop
public class Loops {
    public static void main(String[] args) {
        int i = 0;
        do {
            System.out.println("Value of i is: " + i);
            i++;
        } while (i < 5);
    }
}